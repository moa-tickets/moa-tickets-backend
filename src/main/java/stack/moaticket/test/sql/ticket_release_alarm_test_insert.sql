INSERT IGNORE INTO ticket_alarm (created_at, updated_at, member_id, ticket_id)
SELECT
    NOW(6) AS created_at,
    NOW(6) AS updated_at,
    1 + MOD(((t.rownum - 1) * 50) + s.n, 1000000) AS member_id,
    t.ticket_id
FROM (
         SELECT
             ticket_id,
             ROW_NUMBER() OVER (ORDER BY ticket_id) AS rownum
         FROM ticket
         ORDER BY ticket_id
         LIMIT 100000
     ) t
         JOIN (
    SELECT 0 AS n UNION ALL SELECT 1 UNION ALL SELECT 2 UNION ALL SELECT 3 UNION ALL SELECT 4
